    public void datenhochladen() {

        entitymanager.getTransaction().begin();

        final Game game = Game.getGame_instance();
        entitymanager.persist(game);
        for (int n = 0; n < game.getKat().size(); n++) {
            final Category category = game.getKat().get(n);
            entitymanager.persist(category);
            for (int m = 0; m < game.getKat().get(n).getcFragen().size(); m++) {

                final Question question = game.getKat().get(n).getcFragen().get(m);
                entitymanager.persist(question);
//              for (int w = 0; w < game.getKat().get(n).getcFragen().get(m).getAllAnswers().size(); w++) {
//                  entitymanager.persist(game.getKat().get(n).getcFragen().get(m).getAllAnswers().get(w));
//              }  
            }
        }

        entitymanager.getTransaction().commit();
        entitymanager.close();
        emfactory.close();
    }

