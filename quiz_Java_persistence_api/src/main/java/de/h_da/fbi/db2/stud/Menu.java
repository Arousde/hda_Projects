    private void intitcombo() {

        List resultL = entitymanager.createNamedQuery("Category.findAll").getResultList();
        List<Category> allcat = new ArrayList<>();

        for (Iterator i = resultL.iterator(); i.hasNext();) {
            Category c = (Category) i.next();
            allcat.add(c);
        }

        for (Category c : allcat) {
            Kombocategory.addItem(c.getName());
        }
    }

